        <h1 >Problématique</h1>
        <p> 
			<strong>Problématique énergértique</strong></br></br>
			La production d’énergie mondiale atteint 10 milliards de tonnes équivalent pétrole (tep) chaque année. Elle est assurée essentiellement par du pétrole, du gaz et du charbon, de façon très inégalitaire au niveau de la planète. </br></br>
			Les scénarios énergétiques prévoient une augmentation de 50 à 300% de la production mondiale d’énergie d’ici 2050. Il est d’ores et déjà évident qu’une telle augmentation ne pourra se faire sur le modèle actuel, basé sur les énergies fossiles, dont les réserves sont limitées, et dont l’utilisation conduit à des émissions massives de CO2 responsables d’un changement climatique de grande ampleur.</br></br>
			Le développement de nouvelles sources d’énergie est aujourd’hui incontournable, quelques soient les efforts que nous pourrons faire dans la maîtrise de la demande. Ces sources alternatives sont bien connues et relativement bien quantifiées. Le nucléaire apparaît comme la seule source disponible rapidement à grande échelle, mais nécessite une mobilisation importante de capitaux et une acceptation publique. L’énergie solaire est un gisement important, mais sa mise en œuvre reste extrêmement chère et complexe. Elle est cependant déjà compétitive dans des zones dépourvues de réseaux électriques. </br></br>
			L’énergie éolienne représente un gisement limité et ne pourra sans doute dépasser 10% de la production électrique, et toujours de façon intermittente et aléatoire. La biomasse est une voie intéressante, mais difficile de développer à grande échelle. Les autres sources (géothermie, vagues, marées, …) semblent incapables de répondre à une demande forte. Le stockage de l’énergie (hydrogène notamment) est loin d’être maîtrisé. Il représente un défi technologique important, et pourrait rendre les énergies intermittentes plus intéressantes dans l’avenir. Enfin, la fusion thermonucléaire représente une source massive, mais risque de ne pas être disponible avant la fin du siècle.</br></br>
			<strong>Qu'est-ce qu'un Mix Energétique ?</strong></br></br>
			Le mix énergétique, ou bouquet énergétique, est la répartition des différentes sources d'énergies primaires consommées pour la production des différents types d'énergies.</br></br>
			<strong>MonMIEL, késako ?</strong></br></br>
			Inspiré du <a href="http://revolution-fiscale.fr">simulateur fiscal</a> mis en ligne en 2011, MonMIEL (Mon Mix Electrique) est un site web qui permet de simuler un mix électrique personnalisé.</br></br>
			Cet outil permet également de contribuer à rendre le débat récurrent sur la politique énergétique plus accessible aux citoyens et peut-être même éclairer certains experts.</br></br>
			L'objectif principal de ce site est de sensibiliser l'utilisateur au fait que les contraintes d'équilibrage à tout instant de la consommation et de la production font que tout mix électrique n'est pas toujours réaliste.</br></br>
			Faire comprendre au public que substituer les énergies renouvelables fluctuantes que sont <strong>l'éolien</strong> et <strong>le photo-voltaïque (PV)</strong> aux <strong>réacteurs nucléaires</strong> nécessite de compenser les fluctuations par du <strong>stockage</strong>, des <strong>importations</strong> & des <strong>centrales à flammes</strong> de substitution.
